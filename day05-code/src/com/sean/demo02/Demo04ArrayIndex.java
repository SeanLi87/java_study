/*
 * 数组索引越界异常*/
public class Demo04ArrayIndex {
    public static void main(String[] args) {
        int[] arrayA = {1,2,3};
        System.out.println(arrayA[0]);
        System.out.println(arrayA[1]);
        System.out.println(arrayA[2]);
        System.out.println(arrayA[3]);//索引越界错误
    }
}
